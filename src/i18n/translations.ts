export function t(lang: Lang, key: string, replacements?: Record<string, string | number>): string {
  let str = translations[lang][key] || key
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      str = str.replace(`{${k}}`, String(v))
    }
  }
  return str
}

export function getCatLabel(lang: Lang, cat: string): string {
  return t(lang, `cat_labels_${cat}`)
}

export function getProductDesc(product: { description: string; description_en: string | null }, lang: Lang): string {
  if (lang === 'en' && product.description_en) return product.description_en
  return product.description
}

export function getProductName(product: { name: string; name_en: string | null }, lang: Lang): string {
  if (lang === 'en' && product.name_en) return product.name_en
  return product.name
}
